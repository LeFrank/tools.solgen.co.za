                <h3>What is the expense tool?</h3>

                <p>The expense tool began with a question: <strong>&lsquo;Where did the money go?&rsquo;</strong><br />
                    Phase one of answering this question involved keeping track of expenses.<br />
                    In the early stages, excel spread sheets were used. Which worked brilliantly for a month or two.<br />
                    However after a few months, the data would be separated either as a document<br />
                    per month or a sheet per month.</p>

                <p>Having all expenses for multiple months on one sheet became unwieldily<br />
                    and prone to error when capturing new data. Generating graphs and<br />
                    analyzing over period also proved to be a manual process.</p>

                <p>Once a certain level of separation sets in, it is quite difficult to get a<br />
                    quick and concise understanding of patterns and behaviour.<br />
                    Thus, the expense tool is an attempt to capture the data as one<br />
                    massive dataset, and slicing and dicing the data set based criteria<br />
                    such as date, type of expense, payment method, location and amount.</p>

                <p>With these criterion and a large dataset the ability to find patterns<br />
                    and draw helpful statistics becomes simple and clearly shows<br />
                    <strong>where the money went</strong></p>

                <h2>What does the expense tool do?</h2>

                <h3>Capture Expenses</h3>

                <p>Gives you the ability to track your expenses.<br />
                    Expenses are categorised by type, payment method and location.</p>

                <p>Provides the ability to:</p>

                <h3>Create and Manage Expense Periods</h3>

                <p>Are date ranges for which you would like to track and group expenses.<br />
                    e.g. If you earn a weekly wages, Friday to Friday may be the best way to track and group expenses.<br />
                    Monthly, start on the day you receive your salary and end the day before the next pay date.<br />
                    You can specify what and how long the expense periods are.<br />
                    The periods are used for drawing up budgets and generating statistics.&nbsp;&nbsp; &nbsp;</p>

                <h3>Budget</h3>

                <p>You can create a budget. Setting a limit for the budget period.<br />
                    Describe some of the major planned expenses for this period. E.G. Home/Vehicle repairs, Rent/Mortgage, groceries etc.<br />
                    List a few major expected expenses.</p>

                <p>Create a list of categorized budget limits.</p>

                <p>Each budget item list has three ways of being created.<br />
                    &nbsp;&nbsp; &nbsp;1. Use the expenses from the previous period.<br />
                    &nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;This is usefull as the previous budget period usually consists of +- 40% repeatable expenses and most likely limits will remain more or less the same.<br />
                    &nbsp;&nbsp; &nbsp;2. Use Previous Budget.<br />
                    &nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;This will use the previously configured budget items and limits. The final budgetted item limit can still be altered.<br />
                    &nbsp;&nbsp; &nbsp;3. Use Expense Types<br />
                    &nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;No limits set or brought forward. The user has no prior guidance per category.<br />
                    &nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;This is also the first option which is used for a brand new budget when no prior expenses have been captured.</p>

                <p>After the budget has been created. Go ahead and continue to capture expenses.<br />
                    The budget will reflect the current status of each category. It will show the categories budgetted limit. How much has been spent so far, how much is remaining to be spent in units and percentage.</p>

                <p>The remaining column is colour coded to indicate the health of each category.</p>

                <p>It displays the number of expenses, total spent thus far, total remaining to be spent, and what is projected if all category limits are spent. If there are overages in any one of the categories it will display how much you will be over by at the end of the period. Generally if you have gone over the budget it means, you have either gone into debt ( credit cards, dipping into your savings, loans )</p>

                <p>In general when you go over on one category you should attempt to reduce spending in a different category to remain within the overall budget. You should not alter the budget limits after they have been set. This is specifically done so that categories which have bust the budget remain as warnings for future budgetting and post analysis. We learn most from our mistakes. Especially if those mistakes are not hidden or retroactively corrected.</p>

