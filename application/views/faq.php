            <h3>What is the expense tool?</h3>
            <p>
                The expense tool began with a question: <strong>&lsquo;Where did the money go?&rsquo;</strong><br/>
                Phase one of answering this question involved keeping track of expenses.<br/>
                In the early stages, excel spread sheets were used. Which worked brilliantly for a month or two.<br/>
                However after a few months, the data would be separated either as a document <br/>
                per month or a sheet per month.<br/>
            </p>
            <p>
                Having all expenses for multiple months on one sheet became unwieldily <br/>
                and prone to error when capturing new data. Generating graphs and <br/>
                analyzing over period also proved to be a manual process.
            </p>
            <p>
                Once a certain level of separation sets in, it is quite difficult to get a<br/>
                quick and concise understanding of patterns and behaviour.<br/>
                Thus, the expense tool is an attempt to capture the data as one<br/> 
                massive dataset, and slicing and dicing the data set based criteria <br/>
                such as date, type of expense, payment method, location and amount.
            </p>
            <p>
                With these criterion and a large dataset the ability to find patterns<br/> 
                and draw helpful statistics becomes simple and clearly shows <br/>
                <strong>where the money went</strong>
            </p>

        </li>
        <li>
            <h3>What does the expense tool do?</h3>
            <p>
                The tool is still in progress, it provides a place to record expenses.<br/>
                Once recorded it provides a few ways to analyse the data.<br>
                The history page allows for the filtering of data based on the captured criterion.<br/>
                The statistics page allows for a high level view of expenses over a time period,<br/>
                it also breaks down expenses per criterion. There is the option to drill down<br.>
            further, as well as change the time period.<br/>

            <ul>
                <li>Capture an expense</li>
                <li>Display the history of expenses</li>
                <li>Allows for the creation,editing and deletion of expense types</li>
                <li>Allows for the creation,editing and deletion of payment methods</li>
                <li>Generates Statistics for the passed calendar month period</li>
                <li>Generates Statistics for a 90 day roll up of expenses</li>
                <li>Generates a 60 day forecast of expenses if it detects any patterns or behaviour</li>
            </ul>
            </p>
